class Domains_model extends CI_Model {
    public function __construct()
    {
        $this->load->database();
    }
    public function get_domains($domain=FALSE){
        if(empty($domain)){
            return array();
        }
        $query = $this->db->get_where('dominio', array('dominio' => $domain));
        return $query->row_array();
    }
}